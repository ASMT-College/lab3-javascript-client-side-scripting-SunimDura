// 1️ Using Math Constants
console.log("Math.E:", Math.E);        // 2.718281828459045
console.log("Math.PI:", Math.PI);      // 3.141592653589793
console.log("Math.SQRT2:", Math.SQRT2);  // 1.4142135623730951
console.log("Math.LN2:", Math.LN2);    // 0.6931471805599453
console.log("Math.LN10:", Math.LN10);  // 2.302585092994046

// 2️ Using Math Methods
let num1 = -5.5;
let num2 = 7.8;
let num3 = 16;

console.log("Math.abs(num1):", Math.abs(num1));  // 5.5
console.log("Math.round(num2):", Math.round(num2));  // 8
console.log("Math.floor(num2):", Math.floor(num2));  // 7
console.log("Math.ceil(num2):", Math.ceil(num2));  // 8
console.log("Math.max(3, 5, 8, 2):", Math.max(3, 5, 8, 2));  // 8
console.log("Math.min(3, 5, 8, 2):", Math.min(3, 5, 8, 2));  // 2
console.log("Math.random():", Math.random());  // Random number between 0 and 1
console.log("Math.pow(2, 3):", Math.pow(2, 3));  // 8
console.log("Math.sqrt(num3):", Math.sqrt(num3));  // 4
console.log("Math.sin(Math.PI / 2):", Math.sin(Math.PI / 2));  // 1
console.log("Math.hypot(3, 4):", Math.hypot(3, 4));  // 5 (Pythagorean theorem)
console.log("Math.trunc(5.7):", Math.trunc(5.7));  // 5

// Trigonometric Methods
console.log("Math.asin(1):", Math.asin(1));  // 1.5707963267948966
console.log("Math.acos(-1):", Math.acos(-1)); // 3.141592653589793
console.log("Math.atan(1):", Math.atan(1));  // 0.7853981633974483
